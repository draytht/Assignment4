DROP SCHEMA IF EXISTS `sales` ;
CREATE SCHEMA IF NOT EXISTS `sales` ;
use sales;
CREATE TABLE IF NOT EXISTS salesrep (
    idrep INT AUTO_INCREMENT NOT NULL,
    FirstName VARCHAR(50) NOT NULL,
    LastName VARCHAR(50) NOT NULL,
    PRIMARY KEY (idrep)
);

CREATE TABLE IF NOT EXISTS item (
    iditem INT AUTO_INCREMENT NOT NULL,
    ItemName VARCHAR(50),
    ItemCost DECIMAL,
    PRIMARY KEY (iditem)
);

CREATE TABLE IF NOT EXISTS ledger (
    idledger INT AUTO_INCREMENT NOT NULL,
    idrep INT,
    iditem INT,
    quantity INT,
    purchase_date DATETIME,
    PRIMARY KEY (idledger)
);

CREATE TABLE IF NOT EXISTS region (
    idregion INT NOT NULL AUTO_INCREMENT,
    region_name VARCHAR(50),
    PRIMARY KEY (idregion)
);


CREATE TABLE IF NOT EXISTS repregion (
    idrepregion INT NOT NULL AUTO_INCREMENT,
    idregion INT NOT NULL,
    idrep INT NOT NULL,
    PRIMARY KEY (idrepregion)
);

